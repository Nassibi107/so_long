#include "tools.h"

int	sepone(char **map)
{
	int	y;
	int	x;

	y = 0;
	while (map[y])
		y++;
	y--;
	x = 0;
	while (map[y][x])
	{
		if (map[y][x] != '1')
			return (0);
		x++;
	}
	return (1);
}
