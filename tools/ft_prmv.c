#include "tools.h"

void	ft_prmv(int f)
{
	if (f == 125)
		write(1, "\033[1;31m down", 3);
	else if (f == 126)
		write(1, "\033[1;33m up", 3);
	else if (f == 123)
		write(1, "\033[1;34m  right", 3);
	else if (f == 124)
		write(1, "\033[1;31m left", 3);
}
